namespace Humanizer;

/// <summary>
/// Represents the grammatical number forms used by Lithuanian quantity phrases.
/// </summary>
enum LithuanianNumberForm
{
    /// <summary>Uses the singular form.</summary>
    Singular,
    /// <summary>Uses the plural form.</summary>
    Plural,
    /// <summary>Uses the genitive plural form.</summary>
    GenitivePlural
}
