namespace Humanizer;

/// <summary>
/// Represents the time units supported by Humanizer's relative-time and duration formatters.
/// </summary>
public enum TimeUnit
{
    /// <summary>
    /// One millisecond.
    /// </summary>
    Millisecond,

    /// <summary>
    /// One second.
    /// </summary>
    Second,

    /// <summary>
    /// One minute.
    /// </summary>
    Minute,

    /// <summary>
    /// One hour.
    /// </summary>
    Hour,

    /// <summary>
    /// One day.
    /// </summary>
    Day,

    /// <summary>
    /// One week.
    /// </summary>
    Week,

    /// <summary>
    /// One month.
    /// </summary>
    Month,

    /// <summary>
    /// One year.
    /// </summary>
    Year
}
