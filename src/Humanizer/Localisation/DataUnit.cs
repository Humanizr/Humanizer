namespace Humanizer;

/// <summary>
/// Represents the data units supported by Humanizer's data-size formatting.
/// </summary>
public enum DataUnit
{
    /// <summary>
    /// A bit.
    /// </summary>
    Bit,

    /// <summary>
    /// A byte.
    /// </summary>
    Byte,

    /// <summary>
    /// A kilobyte.
    /// </summary>
    Kilobyte,

    /// <summary>
    /// A megabyte.
    /// </summary>
    Megabyte,

    /// <summary>
    /// A gigabyte.
    /// </summary>
    Gigabyte,

    /// <summary>
    /// A terabyte.
    /// </summary>
    Terabyte
}
